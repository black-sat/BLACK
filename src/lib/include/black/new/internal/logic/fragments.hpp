#ifndef BLACK_LOGIC_FRAGMENTS_HPP_
#define BLACK_LOGIC_FRAGMENTS_HPP_

//
// This file defines the fragments and the namespaces exposed by the library.
// Everything declared before this file in `logic.hpp` have been declared in
// `black::internal::new_api`, while here we re-export a selected number of
// names into the user-facing `black::new_api` namespace
//
//

namespace black::new_api {

  //
  // The `black::new_api::logic` namespace contains all the user-facing names in
  // their generality, including all the fragments.
  //
  namespace logic {
    using black::internal::new_api::alphabet;
    using black::internal::new_api::otherwise;
    using black::internal::new_api::syntax_element;
    using black::internal::new_api::fragment;
    using black::internal::new_api::hierarchy;
    using black::internal::new_api::storage_kind;
    using black::internal::new_api::hierarchy_element;
    using black::internal::new_api::make_fragment_t;
    using black::internal::new_api::make_combined_fragment_t;
    using black::internal::new_api::only;
    using black::internal::new_api::for_each_child;
    using black::internal::new_api::fragment_cast;
    using black::internal::new_api::can_fragment_cast;
    using black::internal::new_api::fragment_unsafe_cast;
    
    //
    // These are three fragments useful for use with `only`
    //
    struct Future : make_fragment_t<
      syntax_element::tomorrow,
      syntax_element::w_tomorrow,
      syntax_element::always,
      syntax_element::eventually,
      syntax_element::until,
      syntax_element::release
    > { };

    struct Past : make_fragment_t<
      syntax_element::yesterday,
      syntax_element::w_yesterday,
      syntax_element::once,
      syntax_element::historically,
      syntax_element::since,
      syntax_element::triggered
    > { };

    struct Temporal : make_combined_fragment_t<Future, Past> { };

    //
    // We include all the names generated by the hierarchy definition file.
    //
    #define declare_hierarchy(Base) \
      using black::internal::new_api::Base;

    #define declare_storage_kind(Base, Storage) \
      using black::internal::new_api::Storage;

    #define declare_hierarchy_element(Base, Storage, Element) \
      using black::internal::new_api::Element;

    #include <black/new/internal/logic/hierarchy.hpp>
  }

  //
  // The `Future`, `Past` and `Temporal` fragments make sense only in a few
  // namespaces.
  //
  namespace LTL {
    using black::new_api::logic::Future;
    using black::new_api::logic::Past;
    using black::new_api::logic::Temporal;
  }
  namespace LTLP {
    using black::new_api::logic::Future;
    using black::new_api::logic::Past;
    using black::new_api::logic::Temporal;
  }
  namespace LTLFO {
    using black::new_api::logic::Future;
    using black::new_api::logic::Past;
    using black::new_api::logic::Temporal;
  }
  namespace LTLPFO {
    using black::new_api::logic::Future;
    using black::new_api::logic::Past;
    using black::new_api::logic::Temporal;
  }
}

//
// Now we define the fragments. Here we define particular macros that enumerate
// all the `syntax_element`s included in the given fragment. These macros will
// be called in the right way later. Here, we treat this piece of code as a sort
// of declarative description of the fragments.
//
 
#define enum_elements_Boolean(Element) \
  Element(boolean) \
  Element(proposition) \
  Element(negation) \
  Element(conjunction) \
  Element(disjunction) \
  Element(implication) \
  Element(iff)

#define enum_elements_FO(Element) \
  enum_elements_Boolean(Element) \
  Element(atom) \
  Element(exists) \
  Element(forall) \
  Element(constant) \
  Element(variable) \
  Element(application) \
  Element(negative) \
  Element(subtraction) \
  Element(addition) \
  Element(multiplication) \
  Element(division) \
  Element(equal) \
  Element(not_equal) \
  Element(less_than) \
  Element(less_than_equal) \
  Element(greater_than) \
  Element(greater_than_equal) \
  Element(integer) \
  Element(real) \
  Element(zero) \
  Element(one) \
  Element(relation) \
  Element(function)

#define enum_elements_LTL(Element) \
  enum_elements_Boolean(Element) \
  Element(tomorrow) \
  Element(w_tomorrow) \
  Element(always) \
  Element(eventually) \
  Element(until) \
  Element(release) \
  Element(w_until) \
  Element(s_release)

#define enum_elements_LTLP(Element) \
  enum_elements_LTL(Element) \
  Element(yesterday) \
  Element(w_yesterday) \
  Element(once) \
  Element(historically) \
  Element(since) \
  Element(triggered)

#define enum_elements_LTLFO(Element) \
  enum_elements_FO(Element) \
  enum_elements_LTL(Element) \
  Element(next) \
  Element(wnext)

#define enum_elements_LTLPFO(Element) \
  enum_elements_LTLFO(Element) \
  enum_elements_LTLP(Element) \
  Element(prev) \
  Element(wprev)

//
// By repeatedly including `declare_fragment.hpp` we actually declare the
// fragments and their namespaces
//
#define FRAGMENT Boolean
#include <black/new/internal/logic/declare_fragment.hpp>
#undef FRAGMENT
#define FRAGMENT FO
#include <black/new/internal/logic/declare_fragment.hpp>
#undef FRAGMENT
#define FRAGMENT LTL
#include <black/new/internal/logic/declare_fragment.hpp>
#undef FRAGMENT
#define FRAGMENT LTLP
#include <black/new/internal/logic/declare_fragment.hpp>
#undef FRAGMENT
#define FRAGMENT LTLFO
#include <black/new/internal/logic/declare_fragment.hpp>
#undef FRAGMENT
#define FRAGMENT LTLPFO
#include <black/new/internal/logic/declare_fragment.hpp>
#undef FRAGMENT

#undef enum_elements_Boolean
#undef enum_elements_FO
#undef enum_elements_LTL
#undef enum_elements_LTLP
#undef enum_elements_LTLFO
#undef enum_elements_LTLPFO
#undef enum_fragments

#endif // BLACK_LOGIC_FRAGMENTS_HPP_
